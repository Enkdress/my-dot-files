-- [[ Install `lazy.nvim` plugin manager ]]
-- Use default neovim data path
local nvim_data = vim.fn.stdpath("data"):gsub("/typescript", "/nvim")
local lazypath = nvim_data .. "/lazy/lazy.nvim"
if not (vim.uv or vim.loop).fs_stat(lazypath) then
	lazypath = vim.fn.stdpath("data") .. "/lazy/lazy.nvim"
end
if not (vim.uv or vim.loop).fs_stat(lazypath) then
	local lazyrepo = "https://github.com/folke/lazy.nvim.git"
	local out = vim.fn.system({ "git", "clone", "--filter=blob:none", "--branch=stable", lazyrepo, lazypath })
	if vim.v.shell_error ~= 0 then
		error("Error cloning lazy.nvim:\n" .. out)
	end
end

local rtp = vim.opt.rtp
rtp:prepend(lazypath)
-- Add all plugin paths from nvim lazy directory
local nvim_lazy = nvim_data .. "/lazy"
local handle = vim.loop.fs_scandir(nvim_lazy)
if handle then
	while true do
		local name = vim.loop.fs_scandir_next(handle)
		if not name then break end
		local path = nvim_lazy .. "/" .. name .. "/lua"
		if vim.loop.fs_stat(path) then
			rtp:prepend(path)
		end
	end
end

